//! For Loop -->>
for(var x = 1; x <= 10; x++) {
    document.write("<h1> Bangladesh </h1>");
}
document.write("<h1> End </h1>");


for(var x = 1; x <= 100; x++) {
    document.write(" " + x);
}
document.write("<h1> End </h1>");


for(var x = 100; x >= 1; x--) {
    document.write(" " + x);
}
document.write("<h1> End </h1>");


for(var x = 1; x <= 99; x = x + 2) {
    document.write(" " + x);
}
document.write("<h1> End </h1>");


for(var x = 2; x <= 100; x = x + 2) {
    document.write(" " + x);
}
document.write("<h1> End </h1>");

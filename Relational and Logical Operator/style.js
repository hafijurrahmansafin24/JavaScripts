//! Relational Operator -->> [>, >=, <, <=, ==, ===, !=, !==]
var num1 = 20;
var num2 = 25;

console.log(num1>num2);
console.log(num1>=num2);
console.log(num1<num2);
console.log(num1<=num2);
console.log(num1==num2);
console.log(num1===num2);
console.log(num1!=num2);
console.log(num1!==num2);

//! Logical Operator -->> [&&, ||, !]
var num1 = 50;
var num2 = 30;
var num3 = 20;

console.log(num1>num2 && num1>num3);
console.log(num1>num2 || num1>num3);
console.log(!true);
console.log(!false);
